# What is the type of the object that these expressions evaluate to?

# (a) False + False
a = False + False
print(type(a))
# INTEGER

# (b) 2 * 3**2.0
b = 2 * 3**2.0
print(type(b))
# FLOAT POINT

# (c) 4 // 2 + 4 % 2
c = 4 // 2 + 4 % 2
print(type(c))
# INTEGER

# (d) 2 + 3 == 4 or 5 >= 5
d = 2 + 3 == 4 or 5 >= 5
print(type(d))
# BOOLEAN
