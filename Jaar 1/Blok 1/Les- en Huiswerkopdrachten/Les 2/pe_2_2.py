# Het bereik van een lijst is het verschil tussen het grootste en het kleinste getal. Maak nu eerst een variabele
# waaraan je een lijst met minimaal 3 verschillende getallen toekent!
getal = [34, 743, -19]

# Schrijf daarna een Python expressie die het bereik van een lijst berekent.
print(max(getal) - min(getal))

# Als bijvoorbeeld variabele list bestaat uit de getallen 3, 7, -2 en 12, dan moet de expressie evalueren naar 14 (
# verschil tussen 12 en -2). Zorg dat de expressie altijd werkt, ook al bestaat de lijst uit andere waarden!
